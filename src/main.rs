fn main() {
    // 整型
    let decimal = 98_222;
    println!("decimal: {}", decimal);
    let hex = 0xff;
    println!("hex: {}", hex);
    let octal = 0o77;
    println!("octal: {}", octal);
    let binary = 0b0000_0010;
    println!("binary: {}", binary);
    let byte = b'A';
    println!("byte: {}", byte);
}
